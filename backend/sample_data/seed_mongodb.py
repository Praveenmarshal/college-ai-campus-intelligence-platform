import pandas as pd
import numpy as np
from pymongo import MongoClient

SAMPLE_DIR = Path(__file__).resolve().parent
MONGO_URI = os.getenv("MONGO_URI", "")
DB_NAME = os.getenv("MONGO_DB_NAME", "campus_ai")


def get_db():
    """Connect to MongoDB and return the database object."""
    if not MONGO_URI:
        print("ERROR: MONGO_URI is not set. Set it in .env or environment variables.")
        sys.exit(1)
    client = MongoClient(MONGO_URI)
    return client[DB_NAME]


def seed_students(db):
    path = SAMPLE_DIR / "students.xlsx"
    if not path.exists():
        print("  SKIP students.xlsx — not found")
        return
    df = pd.read_excel(path, engine="openpyxl")
    df.columns = [c.strip().lower().replace(" ", "_") for c in df.columns]
    now = datetime.now(timezone.utc)
    count = 0
    for _, row in df.iterrows():
        s_id = str(row.get("student_id", "")).strip()
        if not s_id:
            continue
        doc = {
            "student_id": s_id,
            "name": str(row.get("name", "")),
            "email": str(row.get("email", "")),
            "phone": str(row.get("phone", "")),
            "department": str(row.get("department", "General")),
            "batch_year": int(row.get("batch_year", 2026)),
            "semester": int(row.get("semester", 1)),
            "section": str(row.get("section", "A")),
            "cgpa": float(row.get("cgpa", 0.0)),
            "is_hostel": False,
            "updated_at": now,
        }
        db.students.update_one(
            {"student_id": s_id},
            {"$set": doc, "$setOnInsert": {"created_at": now}},
            upsert=True,
        )
        count += 1
    print(f"  [OK] Seeded {count} students")


