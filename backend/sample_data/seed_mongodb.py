def seed_attendance(db):
    path = SAMPLE_DIR / "attendance.xlsx"
    if not path.exists():
        print("  SKIP attendance.xlsx — not found")
        return
    df = pd.read_excel(path, engine="openpyxl")
    df.columns = [c.strip().lower().replace(" ", "_") for c in df.columns]
    db.attendance.delete_many({})
    now = datetime.now(timezone.utc)
    records = []
    for _, row in df.iterrows():
        s_id = str(row.get("student_id", "")).strip()
        if not s_id:
            continue
        status_raw = str(row.get("status", "absent")).strip().lower()
        normalized = "present" if status_raw in {"present", "p", "1", "yes", "true"} else "absent"
        raw_date = row.get("date")
        if isinstance(raw_date, str):
            try:
                date_obj = pd.to_datetime(raw_date).to_pydatetime()
            except Exception:
                date_obj = now
        elif hasattr(raw_date, "to_pydatetime"):
            date_obj = raw_date.to_pydatetime()
        else:
            date_obj = now
        # Ensure timezone-aware
        if date_obj.tzinfo is None:
            date_obj = date_obj.replace(tzinfo=timezone.utc)

        records.append({
            "student_id": s_id,
            "date": date_obj,
            "status": normalized,
            "course": str(row.get("course", "")),
            "department": str(row.get("department", "")),
        })
    if records:
        db.attendance.insert_many(records)
    print(f"  [OK] Seeded {len(records)} attendance records")


